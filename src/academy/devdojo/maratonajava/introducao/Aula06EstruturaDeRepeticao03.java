public class Aula06EstruturaDeRepeticao03 {
    public static void main(String[] args) {
        // Imprimir os primeiros 25 números de um dado número por exemplo 100

        for (int i = 0; i < 100; i++) {
            System.out.println(i);
            if (i == 25) {
                break;
            }
        }

    }
}
